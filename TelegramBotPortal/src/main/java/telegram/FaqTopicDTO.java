package telegram;

public class FaqTopicDTO {

	
private int faqID = 0;
private String faqTopicName = "";
private int userID = 0;

public int getFaqID() {
	return faqID;
}
public void setFaqID(int faqID) {
	this.faqID = faqID;
}
public String getFaqTopicName() {
	return faqTopicName;
}
public void setFaqTopicName(String faqTopicName) {
	this.faqTopicName = faqTopicName;
}
public int getUserID() {
	return userID;
}
public void setUserID(int userID) {
	this.userID = userID;
}


}
